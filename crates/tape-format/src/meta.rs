//! `meta.yaml` schema. See SPEC.md §3.

use serde::{Deserialize, Serialize};
use tape_judge::JudgeCallRecord;

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct Meta {
    pub tape_version: String,
    pub id: String,
    pub created_at: String,
    pub ejected_at: String,
    pub task: String,
    pub recorder: Recorder,
    pub outcome: Outcome,

    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub models: Vec<ModelSummary>,

    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub tools: Vec<ToolSummary>,

    #[serde(skip_serializing_if = "Option::is_none")]
    pub tool_budget: Option<ToolBudget>,

    #[serde(skip_serializing_if = "Option::is_none")]
    pub redaction_summary: Option<RedactionSummary>,

    /// Caller-supplied free-form label for filing / categorisation.
    /// `tape record --label X` populates this; readers can group cassettes
    /// by label without parsing `task`. SPEC §3.2. (Issue #72.)
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub label: Option<String>,

    /// One-or-two-sentence post-investigation summary suitable for pasting
    /// into Slack / Linear / Jira / PR descriptions. Plain UTF-8, ≤280
    /// chars, no newline. Set by `tape recap --set <text>`; cleared by
    /// `tape recap --clear`. Distinct from `task` (pre-investigation
    /// framing) and `liner-notes.md` (200–500 word write-up). (Issue #105.)
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub recap: Option<String>,

    /// Append-only audit trail of every `tape recap` invocation against
    /// this cassette. Each `--set` / `--clear` adds one entry. Empty on
    /// untouched cassettes — same skip-when-empty serde shape as
    /// `models` / `tools`. (Issue #105.)
    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub recaps: Vec<RecapEntry>,

    /// OPTIONAL multi-valued facet field — orthogonal low-cardinality
    /// labels for filing, search, and CI gates. Distinct from `label`
    /// (single string, free-form) in that tags COMPOSE: a cassette can
    /// be `bug-fix` AND `auth` AND `regression-baseline`. Writers SHOULD
    /// emit lowercase kebab-case strings; consumers MUST NOT assume
    /// canonicalisation. Empty list serialises as field-absent so
    /// untouched cassettes stay byte-identical. (Issue #93 Step 1.)
    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub tags: Vec<String>,

    /// Append-only audit trail of every `tape relinernote` invocation
    /// against this cassette. Each successful run adds one entry. Empty
    /// on untouched cassettes — same skip-when-empty serde shape as
    /// `recaps`. The entry carries the SHA-256 of the prior and new
    /// `liner-notes.md` so a reader can reconstruct the regeneration
    /// timeline without keeping every intermediate body. (Issue #71.)
    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub relinernotes: Vec<RelinernoteEntry>,

    /// Append-only audit trail of every `tape compact` invocation against
    /// this cassette. Each invocation adds one entry — including no-op
    /// runs where nothing changed, so the audit log captures the
    /// "compact was attempted with these parameters" event independently
    /// of whether any payload was actually truncated. Empty on untouched
    /// cassettes — same skip-when-empty serde shape as `recaps` /
    /// `relinernotes` so existing fixtures stay byte-identical.
    /// (Issue #51 Phase 2, carved per #244.)
    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub compactions: Vec<CompactionEntry>,

    /// Provenance record for cassettes generated by `tape new`. Present
    /// only on tapes the `new` subcommand wrote; consumers don't fill
    /// this in for live recordings or for tapes coming through the
    /// eject pipeline. SPEC §11 forward-compat: older readers ignore
    /// unknown top-level keys. (Issue #99.)
    ///
    /// Stored as `meta.new` in the YAML; the Rust field is `new_block`
    /// because `new` is a reserved keyword.
    #[serde(rename = "new", skip_serializing_if = "Option::is_none", default)]
    pub new_block: Option<NewBlock>,
}

/// What `tape new` records about how a cassette was generated. Distinct
/// from `recorder` (live capture) and from `recaps` (post-hoc CLI
/// edits). The template id + version pair lets a future ticket compare
/// against a registry of bundled templates ("am I looking at a
/// pristine `minimal` template, or has the user edited it?").
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct NewBlock {
    pub template_id: String,
    pub template_version: String,
    pub generated_at: String,
    /// Names of placeholders that were filled in at generation time
    /// (Step 1 of #99 only fills `task`, but the slot is recorded so a
    /// future step can describe richer substitutions without changing
    /// the schema).
    #[serde(skip_serializing_if = "Vec::is_empty", default)]
    pub placeholders_filled: Vec<String>,
}

/// One row in the `meta.recaps` audit array. Records the wall-clock time
/// of the operation, which side it modified (set vs clear vs auto), and
/// the before/after recap text so a reader can reconstruct the recap
/// timeline without re-running the operations. For `Auto` entries the
/// embedded `judge_call` captures the underlying LLM invocation's audit
/// row (model, prompt/output hashes, scan result, retries, truncation).
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct RecapEntry {
    pub applied_at: String,
    pub kind: RecapKind,
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub prior_recap: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub new_recap: Option<String>,
    /// LLM audit record for entries written by `tape recap --auto`. Always
    /// `None` on `Set` / `Clear` rows; populated on `Auto` rows from the
    /// `JudgeCallRecord` returned by the underlying `tape-judge` call.
    /// Skipped from the YAML when absent so existing fixtures stay byte-
    /// identical. (Issue #151.)
    #[serde(skip_serializing_if = "Option::is_none", default)]
    pub judge_call: Option<JudgeCallRecord>,
}

#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "lowercase")]
pub enum RecapKind {
    /// `tape recap --set <text>`; `prior_recap` is the pre-existing value
    /// (None on first set), `new_recap` is the value being written.
    Set,
    /// `tape recap --clear`; `prior_recap` is what existed,
    /// `new_recap` is None.
    Clear,
    /// `tape recap --auto`; `prior_recap` is the pre-existing value (if
    /// any), `new_recap` is the model-authored recap that just passed
    /// validation and the defense-in-depth scanner. The `judge_call`
    /// field on the entry holds the LLM audit row. (Issue #151.)
    Auto,
}

/// One row in the `meta.relinernotes` audit array. Records when the
/// regeneration ran, which model + bundled-template id was used, and
/// the SHA-256 of the `liner-notes.md` body on both sides of the swap
/// so a chain of two or more runs can be verified by hash equality
/// (`entry[N].prior_liner_notes_sha256 == entry[N-1].new_liner_notes_sha256`).
/// `judge_call` is the audit row returned by `tape_judge::JudgeClient::complete`.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct RelinernoteEntry {
    pub applied_at: String,
    pub model: String,
    /// Bundled-template id. Always `"default"` in Phase 1 of #71; the
    /// later `--template-id` flag introduces `terse` / `regulatory` /
    /// `pedagogical` / `merged`.
    pub template_id: String,
    /// `sha256(prior liner-notes.md bytes)` (lowercase hex, no
    /// prefix). Used to verify an append-only chain across multiple
    /// `tape relinernote` runs on the same lineage.
    pub prior_liner_notes_sha256: String,
    /// `sha256(new liner-notes.md bytes)` (lowercase hex, no prefix).
    pub new_liner_notes_sha256: String,
    pub judge_call: JudgeCallRecord,
}

/// One row in the `meta.compactions` audit array. Records when a
/// `tape compact` invocation ran, which compaction kind it applied,
/// the `--max-output-chars` ceiling in effect, and the step numbers
/// of tracks whose payload actually changed. Same shape as
/// `RecapEntry` / `RelinernoteEntry` so consumers can reuse the
/// applied-at iteration pattern. (Issue #51 Phase 2, carved per #244.)
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct CompactionEntry {
    /// RFC 3339 UTC timestamp with millisecond precision (matches
    /// the `applied_at` shape used by `RecapEntry` /
    /// `RelinernoteEntry`).
    pub applied_at: String,
    pub kind: CompactionKind,
    /// The `--max-output-chars` value in effect for this invocation.
    /// Captured even on no-op runs so an auditor can reconstruct
    /// "what threshold was being applied at time T".
    pub max_chars: usize,
    /// Step numbers of tracks whose payload was modified by this
    /// invocation. Empty on a no-op run (the entry is still
    /// recorded — see the `compactions` field doc comment).
    #[serde(default)]
    pub tracks_affected: Vec<u64>,
}

/// Phase-2 compaction kind. Single-variant on purpose: pins the
/// JSON shape (`kind: truncate_output`) so Phase 3+ can add
/// `DropKind`, `RewriteStub`, etc. without breaking older readers.
/// Mirrors the `RecapKind` forward-compat pattern.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum CompactionKind {
    /// `tape compact` truncated one or more oversize string payload
    /// values past `--max-output-chars`. Phase 1 of #51's single
    /// behavior; Phase 3+ adds `DropKind` etc.
    TruncateOutput,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct Recorder {
    pub agent: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub user: Option<String>,
}

#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "lowercase")]
pub enum Outcome {
    Success,
    Failure,
    Abandoned,
    Unknown,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct ModelSummary {
    pub vendor: String,
    pub model: String,
    pub calls: u64,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct ToolSummary {
    pub kind: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub server: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub tool: Option<String>,
    pub calls: u64,
}

#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Default)]
pub struct ToolBudget {
    pub total_calls: u64,
    pub total_tokens_in: u64,
    pub total_tokens_out: u64,
    pub wall_clock_ms: u64,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct RedactionSummary {
    pub rules_applied: Vec<String>,
    pub redaction_count: u64,
}

impl Meta {
    /// Parse `meta.yaml` content.
    pub fn parse(yaml: &str) -> crate::Result<Self> {
        Ok(serde_yaml::from_str(yaml)?)
    }

    /// Serialize to YAML.
    pub fn to_yaml(&self) -> crate::Result<String> {
        Ok(serde_yaml::to_string(self)?)
    }
}

/// Maximum length for `meta.recap`. SPEC-adjacent constant kept here next
/// to the field it bounds so callers (the CLI write path and `tape
/// verify`) share one source of truth. (Issue #105.)
pub const RECAP_MAX_LEN: usize = 280;

/// Validate a candidate `meta.recap` value against the on-disk schema
/// invariants: ≤[`RECAP_MAX_LEN`] chars, no newline characters. Returns
/// a human-readable error message on violation. Used by both the
/// `tape recap --set` CLI write path and the `tape verify` read path so
/// any source of a recap (CLI today, deck tool tomorrow, hand-written
/// cassettes from third-party tooling) is held to the same rule.
pub fn validate_recap_text(s: &str) -> Result<(), String> {
    let len = s.chars().count();
    if len > RECAP_MAX_LEN {
        return Err(format!("recap exceeds {RECAP_MAX_LEN} chars: got {len}"));
    }
    if s.contains('\n') || s.contains('\r') {
        return Err("recap must not contain newline characters".into());
    }
    Ok(())
}

