    #[test]
    fn compaction_entry_round_trips_with_lowercase_snake_kind() {
        let mut meta = fresh_meta();
        meta.compactions.push(CompactionEntry {
            applied_at: "2026-05-16T10:00:00.000Z".into(),
            kind: CompactionKind::TruncateOutput,
            max_chars: 1024,
            tracks_affected: vec![3, 5, 9],
        });
        let yaml = meta.to_yaml().unwrap();
        // Pinning: enum is rename_all = "snake_case", so the kind
        // round-trips as `truncate_output` (not `TruncateOutput`).
        assert!(
            yaml.contains("kind: truncate_output"),
            "expected snake_case kind in: {yaml}"
        );
        let parsed = Meta::parse(&yaml).unwrap();
        assert_eq!(parsed.compactions.len(), 1);
        assert_eq!(parsed.compactions[0].kind, CompactionKind::TruncateOutput);
        assert_eq!(parsed.compactions[0].max_chars, 1024);
        assert_eq!(parsed.compactions[0].tracks_affected, vec![3, 5, 9]);
        assert_eq!(parsed.compactions[0].applied_at, "2026-05-16T10:00:00.000Z");
    }

    #[test]
    fn compactions_array_appends_in_order_across_two_entries() {
        let mut meta = fresh_meta();
        meta.compactions.push(CompactionEntry {
            applied_at: "2026-05-16T10:00:00.000Z".into(),
            kind: CompactionKind::TruncateOutput,
            max_chars: 1024,
            tracks_affected: vec![2],
        });
        meta.compactions.push(CompactionEntry {
            applied_at: "2026-05-16T10:05:00.000Z".into(),
            kind: CompactionKind::TruncateOutput,
            max_chars: 512,
            tracks_affected: vec![2, 4],
        });
        let yaml = meta.to_yaml().unwrap();
        let parsed = Meta::parse(&yaml).unwrap();
        assert_eq!(parsed.compactions.len(), 2);
        assert_eq!(parsed.compactions[0].max_chars, 1024);
        assert_eq!(parsed.compactions[1].max_chars, 512);
        assert_eq!(parsed.compactions[1].tracks_affected, vec![2, 4]);
        assert!(parsed.compactions[0].applied_at < parsed.compactions[1].applied_at);
    }

